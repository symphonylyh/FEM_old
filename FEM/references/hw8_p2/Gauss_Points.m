function GP = Gauss_Points(ord,El)
%-------------------------------------------------------------------------%
%Description:
% This function contains the locations, weiths and number of Gauss points 
% for exact numerical integration of order ord over various domains
% 
%Synopsis: GP = Gauss_Points(ord,El)
%
%Parameters: 
% Input: ord, order of exact quadrature (of the underlying 1D quadrature 
%        rule for 2D square domains)
% Input: El, structure array containing element information 
%
% Output: GP, structure array containing the number of Gauss points, 
%            their locations and corresponding weights
%
% Structure Variable: GP.N, number of Gauss points
% Structure Variable: GP.W, (GP.N,1) column vector containing theweights of 
%                     the Gauss points
% Structure Variable: GP.Xi, (GP.N,dim) array containing the canonical
%                     coordinates of the Gauss points in the canonical
%                     domain
%-------------------------------------------------------------------------%
    switch(El.Type(1))
        case 'B'
            if ord==1                
                GP.('Xi')=[0];
                GP.('W')=[2];                
            elseif ord==3
                GP.('Xi')=1/sqrt(3)*[1 -1]';
                GP.('W')=[1 1]';
            elseif ord==5
                GP.('Xi')=[0 sqrt(15)/5*[1 -1]]';
                GP.('W')=[8/9 5/9*[1 1]]';
            elseif ord==7
                GP.('Xi')=[sqrt(525-70*sqrt(30))/35*[1 -1] sqrt(525+70*sqrt(30))/35*[1 -1]]';
                GP.('W')=[(18+sqrt(30))/36*[1 1] (18-sqrt(30))/36*[1 1]]';
            elseif ord==9
                GP.('Xi')=[0 sqrt(245-14*sqrt(70))/21*[1 -1] sqrt(245+14*sqrt(70))/21*[1 -1]]';
                GP.('W')=[128/225 (322+13*sqrt(70))/900*[1 1] (322-13*sqrt(70))/900*[1 1]]';
            else
                error('Unknown order')
            end
        case 'T'
            w=1/2;
            if ord==1
                GP.('Xi')=[1/3 1/3];
                GP.('W')=w;
            elseif ord==2
                GP.('Xi')=[1/6 1/6;
                           2/3 1/6;
                           1/6 2/3];
                GP.('W')=w*[1/3 1/3 1/3];
            elseif ord==3
                GP.('Xi')=[1/3 1/3;
                           1/5 1/5;
                           3/5 1/5;
                           1/5 3/5];
                GP.('W')=w*[-27/48 25/48 25/48 25/48];
            elseif ord==4
                GP.('Xi')=[0.108103018168070 0.445948490915965;
                           0.445948490915965 0.108103018168070;
                           0.445948490915965 0.445948490915965;
                           0.816847572980459 0.091576213509771;
                           0.091576213509771 0.816847572980459;
                           0.091576213509771 0.091576213509771];
                GP.('W')=w*[0.223381589678011 0.223381589678011 0.223381589678011...
                            0.109951743655322 0.109951743655322 0.109951743655322]; 
            elseif ord==5
                GP.('Xi')=[1/3 1/3;
                           0.059715871789770 0.470142064105115;
                           0.470142064105115 0.059715871789770;
                           0.470142064105115 0.470142064105115;
                           0.797426985353087 0.101286507323456;
                           0.101286507323456 0.797426985353087;
                           0.101286507323456 0.101286507323456];
                GP.('W')=w*[0.225 0.132394152788506*ones(1,3) 0.125939180544827*ones(1,3)];                
            else
                error('Unknown order')
            end
        case 'Q'            
            if ord==1
                GP.('Xi')=[0 0];
                GP.('W')=2;
            elseif ord==3
                GP.('Xi')=[1/sqrt(3) 1/sqrt(3);
                    1/sqrt(3) -1/sqrt(3);
                    -1/sqrt(3) 1/sqrt(3);
                    -1/sqrt(3) -1/sqrt(3)];
                GP.('W')=[1 1 1 1];
            elseif ord==5
                GP.('Xi')=[0.774596669200000,0.774596669200000,0.774596669200000,0.0,0.0,0.0,-0.774596669200000,-0.774596669200000,-0.774596669200000;...
                           0.774596669200000,0.0,-0.774596669200000,0.774596669200000,0.0,-0.774596669200000,0.774596669200000,0.0,-0.774596669200000]';
                GP.('W')=[0.308641975358025,0.493827160539506,0.308641975358025,0.493827160539506,0.790123456809877,...
                          0.493827160539506,0.308641975358025,0.493827160539506,0.308641975358025]; 
            elseif ord==7
                GP.('Xi')=[0.861136311600000,0.861136311600000,0.861136311600000,0.861136311600000,...
                           0.339981043600000,0.339981043600000,0.339981043600000,0.339981043600000,...
                           -0.339981043600000,-0.339981043600000,-0.339981043600000,-0.339981043600000,...
                           -0.861136311600000,-0.861136311600000,-0.861136311600000,-0.861136311600000;...
                           0.861136311600000,0.339981043600000,-0.339981043600000,-0.861136311600000,...
                            0.861136311600000,0.339981043600000,-0.339981043600000,-0.861136311600000,...
                            0.861136311600000,0.339981043600000,-0.339981043600000,-0.861136311600000,...
                            0.861136311600000,0.339981043600000,-0.339981043600000,-0.861136311600000]';
                GP.('W')=[0.121002993259545,0.226851851840455,0.226851851840455,0.121002993259545,...
                           0.226851851840455,0.425293303059545,0.425293303059545,0.226851851840455,...
                           0.226851851840455,0.425293303059545,0.425293303059545,0.226851851840455,...        
                           0.121002993259545,0.226851851840455,0.226851851840455,0.121002993259545];
            else
                error('Unknown order')
            end
        otherwise
            error('Unknown element type')
    end
    GP.('N')=size(GP.('Xi'),1);
end